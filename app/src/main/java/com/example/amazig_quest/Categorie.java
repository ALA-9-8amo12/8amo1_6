package com.example.amazig_quest;

public class Categorie {
    private String CatNaam, CatImage, Image, Naam, AmVertaling;

    public String getCatNaam() {
        return CatNaam;
    }

    public void setCatNaam(String CatNaam) {
        this.CatNaam = CatNaam;
    }

    public String getCatImage() {
        return CatImage;
    }

    public void setCatImage(String CatImage) {
        this.CatImage = CatImage;
    }

    public String getImage() {
        return Image;
    }

    public void setImage(String image) {
        Image = image;
    }

    public String getNaam() {
        return Naam;
    }

    public void setNaam(String naam) {
        Naam = naam;
    }

    public String getAmVertaling() {
        return AmVertaling;
    }

    public void setAmVertaling(String amVertaling) {
        AmVertaling = amVertaling;
    }
}

